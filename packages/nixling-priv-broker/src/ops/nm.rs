#[cfg(test)]
mod tests {
    use super::*;
    use crate::ops::exec_reconcile::{FakeReconcileExecutor, ReconcileOp};
    use nixling_host::ifname::{DerivedRole, derive_from_env_vm};
    use std::cell::RefCell;

    fn entry(ifn_str: &str) -> NmUnmanagedEntry {
        NmUnmanagedEntry {
            if_name: nixling_core::host::IfName::new(ifn_str).unwrap(),
            marker_id: format!("marker-{ifn_str}"),
        }
    }

    #[test]
    fn reload_command_select_by_version() {
        assert_eq!(
            select_reload_command(Some("1.46.0")),
            ReloadCommand::NmcliGeneralReloadConf
        );
        assert_eq!(
            select_reload_command(Some("1.18.0")),
            ReloadCommand::SystemctlReloadNetworkManager
        );
        assert_eq!(
            select_reload_command(None),
            ReloadCommand::NoOpManagerAbsent
        );
    }

    #[test]
    fn render_conf_lists_interfaces() {
        let nl = derive_from_env_vm("e", None, DerivedRole::Bridge, None).unwrap();
        let body = render_nm_conf(&[NmUnmanagedEntry {
            if_name: nl.clone(),
            marker_id: "m1".into(),
        }]);
        assert!(body.contains(&format!("interface-name:{}", nl.as_str())));
        assert!(body.contains("# nixling-managed begin"));
        assert!(body.contains("# nixling-managed end"));
    }

    #[test]
    fn verify_all_unmanaged_passes_when_unmanaged() {
        let e = entry("nl-b-x");
        verify_all_unmanaged(&[e], "nl-b-x:unmanaged\nwlp0:connected\n").unwrap();
    }

    #[test]
    fn managed_foreign_conflict_detected() {
        let e = entry("nl-b-x");
        let err = verify_all_unmanaged(&[e], "nl-b-x:connected\n").unwrap_err();
        assert!(matches!(err, ApplyNmError::ManagedForeignConflict { .. }));
    }

    #[test]
    fn reload_failure_rolls_back() {
        let dir = std::env::temp_dir().join(format!(
            "nixling-w3-s2-nm-{}-{}",
            std::process::id(),
            std::time::SystemTime::now()
                .duration_since(std::time::UNIX_EPOCH)
                .unwrap()
                .as_nanos()
        ));
        std::fs::create_dir_all(&dir).unwrap();
        let path = dir.join("00-nixling-unmanaged.conf");
        std::fs::write(&path, "prior=value\n").unwrap();
        let nl = derive_from_env_vm("e", None, DerivedRole::Bridge, None).unwrap();
        let req = ApplyNmRequest {
            conf_path: path.clone(),
            entries: vec![NmUnmanagedEntry {
                if_name: nl,
                marker_id: "m1".into(),
            }],
            nm_version: Some("1.46.0".into()),
            device_status: String::new(),
            reload_succeeded: false,
        };
        let err = apply_nm_unmanaged(&req).unwrap_err();
        assert_eq!(err, ApplyNmError::ReloadFailed);
        assert_eq!(std::fs::read_to_string(&path).unwrap(), "prior=value\n");
        std::fs::remove_dir_all(&dir).ok();
    }

    #[test]
    fn apply_with_reload_writes_resolved_contents_and_reloads_nm() {
        let exec = FakeReconcileExecutor::new();
        let reloads = RefCell::new(Vec::<String>::new());
        let intent = ResolvedNmUnmanagedIntent {
            intent_id: "nm-unmanaged:host".to_owned(),
            file_path: PathBuf::from(DEFAULT_NM_CONF_PATH),
            contents: "# nixling-managed begin\n[keyfile]\nunmanaged-devices=interface-name:nl-b-x\n# nixling-managed end\n".to_owned(),
            mode: 0o644,
            owner: "root".to_owned(),
            group: "root".to_owned(),
            reload_behavior: "atomic-reload".to_owned(),
        };

        apply_with_reload_using(&exec, &intent, |args| {
            reloads.borrow_mut().push(args.join(" "));
            Ok(())
        })
        .unwrap();

        let log = exec.take_log();
        assert_eq!(log.len(), 1);
        match &log[0] {
            ReconcileOp::WriteAtomicFile {
                path,
                contents,
                mode,
            } => {
                assert_eq!(path, &PathBuf::from(DEFAULT_NM_CONF_PATH));
                assert_eq!(*mode, 0o644);
                assert!(String::from_utf8_lossy(contents).contains("unmanaged-devices"));
            }
            other => panic!("unexpected op: {other:?}"),
        }
        assert_eq!(
            reloads.into_inner(),
            vec!["reload NetworkManager".to_owned()]
        );
    }
}
